#include <cstdio>

int main() {
    int num1 = 50;
    float num2 = 20.5f;
    char letter = 'a';
    bool on = false;
    long bignum1 = 5000000000000;
    double bignum2 = 1234567890.123456789;

    printf("num1: %d\n", num1);
    printf("num2: %f\n", num2);
    printf("letter: %c\n", letter);
    printf("on: %b\n", on);
    printf("bignum1: %l\n", bignum1);
    printf("bignum2: %f\n", bignum2);

    return 0;
}